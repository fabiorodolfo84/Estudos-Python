#crie um script Python que leia dois numeros e tente mostrar a soa entre els.

num1 = int(input("Primeiro numero: \n"))
num2 = int(input("Segundo numero: \n"))

print ("A soma é:", num1 + num2)
print ("A soma é: %d!" % (num1 + num2))
print ("A soma é: {}!".format(num1+num2))

